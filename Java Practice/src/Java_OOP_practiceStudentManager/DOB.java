package Java_OOP_practiceStudentManager;

public class DOB {
	private int day, month, year;

	//Constructor
	public DOB(int day, int month, int year) {
		this.day = day;
		this.month = month;
		this.year = year;
	}

	//Getter & Setter
	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}
	
	
}
